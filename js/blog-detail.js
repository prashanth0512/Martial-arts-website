const ALL_BLOG_IDS = Object.keys(BLOGS);


function getBlogParam() {
  return new URLSearchParams(window.location.search).get('blog') || 'karate-basics';
}

function buildRelatedCard(id) {
  const b = BLOGS[id];
  if (!b) return '';
  return `
    <a href="blog-detail.html?blog=${b.id}" class="bd-related-card">
      <img src="${b.coverImg}" alt="${b.title}" loading="lazy">
      <div class="bd-related-card-body">
        <span><i class="fa-solid ${b.categoryIcon}"></i> ${b.category}</span>
        <h5>${b.title}</h5>
        <p>${b.readTime}</p>
      </div>
    </a>`;
}


function renderBlog(key) {
  const b = BLOGS[key] || BLOGS['karate-basics'];

  document.title = `${b.title} – GELUD Blog`;

  document.querySelector('.bd-hero-cat').innerHTML = `<i class="fa-solid ${b.categoryIcon}"></i> ${b.category}`;
  document.querySelector('.bd-hero-title').textContent = b.title;
  document.querySelector('.bd-hero-date').textContent = b.date;
  document.querySelector('.bd-hero-read').textContent = b.readTime;
  document.querySelector('.bd-hero-author-name').textContent = b.author;
  document.querySelector('.bd-breadcrumb-post').textContent = b.category + ' Blog';

  const cover = document.querySelector('.bd-cover');
  cover.src = b.coverImg;
  cover.alt = b.title;

  document.querySelector('.bd-content').innerHTML = b.content;

  document.querySelector('.bd-author img').src = b.authorImg;
  document.querySelector('.bd-author img').alt = b.author;
  document.querySelector('.bd-author h5').textContent = b.author;
  document.querySelector('.bd-author p').textContent = b.authorBio;

  const relatedIds = (b.related || []).slice(0,4);
  document.querySelector('.bd-related-grid').innerHTML = relatedIds.map(buildRelatedCard).join('');

  const recentIds = ALL_BLOG_IDS.filter(id => id !== key).slice(0, 5);
  document.querySelector('.bd-sidebar-recent').innerHTML = recentIds.map(id => {
    const rb = BLOGS[id];
    return `<li><a href="blog-detail.html?blog=${rb.id}"><i class="fa-solid fa-caret-right"></i>${rb.title}</a></li>`;
  }).join('');
}


document.addEventListener('DOMContentLoaded', () => {
  renderBlog(getBlogParam());
});
